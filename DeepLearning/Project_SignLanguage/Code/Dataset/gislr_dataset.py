import os
from kaggle.api.kaggle_api_extended import KaggleApi

# Initialize the Kaggle API
api = KaggleApi()
api.authenticate()

# Download the dataset
competition_name = "asl-signs"
download_path = "/kaggle/input/asl-signs"
os.makedirs(download_path, exist_ok=True)

api.competition_download_files(competition_name, path=download_path)

# Unzip the dataset
os.system(f"unzip {os.path.join(download_path, 'asl-signs.zip')} -d {download_path}")

print("Dataset downloaded and extracted successfully!")